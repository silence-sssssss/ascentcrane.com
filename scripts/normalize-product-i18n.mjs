function applyMap(source, map) {
  let output = source;
  for (const [from, to] of Object.entries(map.headers)) output = output.replaceAll(from, to);
  for (const [from, to] of Object.entries(map.values)) output = output.replaceAll(from, to);
  return output;
}

for (const lang of targetLangs) {
  const dir = path.join(root, lang);
  for (const fileName of fs.readdirSync(dir)) {
    if (!fileName.endsWith('.md')) continue;
    const fullPath = path.join(dir, fileName);
    const source = fs.readFileSync(fullPath, 'utf8');
    const updated = applyMap(source, maps[lang]);
    if (updated !== source) fs.writeFileSync(fullPath, updated);
  }
}

